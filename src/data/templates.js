export function detectCategory(prompt) {
  const p = prompt.toLowerCase();
  if (p.includes('studio'))                            return 'studio';
  if (p.includes('4 bed') || p.includes('4-bed') || p.includes('four bed')) return '4br';
  if (p.includes('3 bed') || p.includes('3-bed') || p.includes('three bed')) return '3br';
  if (p.includes('2 bed') || p.includes('2-bed') || p.includes('two bed'))  return '2br';
  if (p.includes('1 bed') || p.includes('1-bed') || p.includes('one bed'))  return '1br';
  if (p.includes('duplex') || p.includes('townhouse'))                       return 'duplex';
  if (p.includes('villa') || p.includes('mansion'))                          return 'villa';
  if (p.includes('office') || p.includes('workspace') || p.includes('cowork')) return 'office';
  return null;
}
